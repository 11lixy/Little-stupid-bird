tourism = ['beijing','nanjing','changsha','shanghai','suzhou']
print(tourism)
print(sorted(tourism))#使用sorted()函数使列表按字母顺序排列
print(tourism)
print(sorted(tourism,reverse=True))#按与字母顺序相反的顺序打印这个列表
print(tourism)
tourism.reverse()#是把原列表中的元素顺序从左至右的重新存放
print(tourism)
tourism.reverse()#再次把列表中的元素顺序从左至右的重新存放
print(tourism)
tourism.sort()#把列表中的元素按字母顺序永久排列
print(tourism)
tourism.sort(reverse=True)#使上面的列表变成相反顺序的列表，同样是永久性的
print(tourism)






